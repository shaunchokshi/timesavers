# ANSI color/style escape sequences as environment variables.
# Source from bash or zsh:  source colors.env
# Then use:  echo "${ANSI_RED}hello${ANSI_RESET}"
#
# Naming:
#   - 16 basic colors use HTML/CSS primary names in UPPERCASE.
#     Foreground:    ANSI_<COLOR>
#     Bold:          ANSI_<COLOR>_BOLD
#     Underline:     ANSI_<COLOR>_UNDERLINE
#     Background:    ANSI_BACKGROUND_<COLOR>
#   - 256-color palette uses xterm CamelCase names from
#     https://www.ditig.com/256-colors-cheat-sheet
#     Foreground:    ANSI_<XtermName>
#     Background:    ANSI_BACKGROUND_<XtermName>
#     Where the xterm chart lists the same name at multiple indices,
#     the index is appended (e.g. ANSI_DeepSkyBlue4_23, _24, _25).
#   - Style modifiers (bold, italic, etc.) are ANSI_<MODIFIER>.
#
# Values use bash/zsh ANSI-C quoting ($'...') so the escape character
# is embedded literally - no need for `echo -e` or `printf '%b'`.

